CREATE TABLE  "EMPLOYEEE" 
   (	"EMPNAME" VARCHAR2(15) NOT NULL ENABLE, 
	"EMPSALARY" NUMBER(10,0), 
	"EMPDESIGNATION" VARCHAR2(15), 
	"EMPID" NUMBER(4,0)
   ) ;

CREATE TABLE  "PHYSICIAN" 
   (	"PNAME" VARCHAR2(15) NOT NULL ENABLE, 
	"PDEPARTMENT" VARCHAR2(15), 
	"PID" NUMBER(4,0)
   ) ;

CREATE TABLE  "PATIENT" 
   (	"PATIENTID" NUMBER(4,0), 
	"PATIENTFNAME" VARCHAR2(15) NOT NULL ENABLE, 
	"PATIENTLNAME" VARCHAR2(15) NOT NULL ENABLE, 
	"PADDRESS" VARCHAR2(15), 
	"SEX" CHAR(1), 
	"PATIENTDATEOFADMISSION" VARCHAR2(50), 
	"PATIENTDATEOFDISCHARGE" VARCHAR2(50)
   ) ;



CREATE TABLE  "NURSE" 
   (	"EMPLOYEEID" NUMBER(4,0), 
	"NURSE_NAME" VARCHAR2(15) NOT NULL ENABLE
   ) ;

CREATE TABLE  "RECEPTIONIST" 
   (	"EMPLOYEEID" NUMBER(4,0), 
	"RECEPTIONIST_NAME" VARCHAR2(15) NOT NULL ENABLE
   ) ;

CREATE TABLE  "WARDS" 
   (	"ROOMID" NUMBER(20,0), 
	"PATIENTID" NUMBER(20,0), 
	"RTYPE" VARCHAR2(15)
   ) ;

